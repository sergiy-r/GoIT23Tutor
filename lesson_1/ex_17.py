# Скласти програму, яка вводить з клавіатури чотиризначне число і виводить на
# екран середнє арифметичне його цифр.

# 1345
x = int(input("Введіть чотиризначне число: "))
x_4 = x % 10  # Останню цифру
x_3 = x // 10 % 10  # Другу цифра зправа
x_2 = x // 100 % 10  # Другу цифра зліва
x_1 = x // 1000 % 10  # Перша цифра

print(f"X is equal: {x}")
print(f"Остання: {x_4}")
print(f"Передостання: {x_3}")
print(f"Друга: {x_2}")
print(f"Перша: {x_1}")
avg = (x_1 + x_2 + x_3 + x_4) / 4
print(avg)

